def analyze_symptoms(user_text):
    text = user_text.lower().strip()
    
    if not text:
        return {
            "specialists": ["Терапевт"],
            "warning": None,
            "is_urgent": False,
            "matched_keywords": [],
            "message": "Пожалуйста, опишите ваш симптом."
        }
    
    matches = []
    
    for keyword, specialists in SYMPTOM_MAP.items():
        if keyword in text:
            for spec in specialists:
                matches.append((spec, keyword))
    
    seen_specialists = set()
    unique_specialists = []
    matched_keywords = []
    
    for spec, keyword in matches:
        if spec not in seen_specialists:
            seen_specialists.add(spec)
            unique_specialists.append(spec)
        if keyword not in matched_keywords:
            matched_keywords.append(keyword)
    
    if not unique_specialists:
        return {
            "specialists": ["Терапевт"],
            "warning": None,
            "is_urgent": False,
            "matched_keywords": [],
            "message": "Рекомендуем начать с терапевта. Он проведёт осмотр и направит к узкому специалисту."
        }
    
    warning = None
    is_urgent = False
    
    for combo in EMERGENCY_COMBOS:
        if all(kw in text for kw in combo["keywords"]):
            warning = combo["message"]
            is_urgent = True
            if combo["specialist"] not in unique_specialists:
                unique_specialists.insert(0, combo["specialist"])
            break
    
    unique_specialists = unique_specialists[:3]
    
    return {
        "specialists": unique_specialists,
        "warning": warning,
        "is_urgent": is_urgent,
        "matched_keywords": matched_keywords,
        "message": None
    }